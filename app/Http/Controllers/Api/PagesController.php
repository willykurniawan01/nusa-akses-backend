        $result['data'] = [];


        if ($pages = Page::all()) {
            foreach ($pages as $eachPage) {
                $eachPage->picture = url(Storage::url($eachPage->picture));
                array_push($result["data"], $eachPage);
            }
        }

        // dd($pages);

        return response()->json($result);
    }

